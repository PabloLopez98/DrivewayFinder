package pablo.myexample.drivewayfinder;

public class OneCalendarObject {

    private String Date;

    //required empty constructor
    public OneCalendarObject(){}

    public OneCalendarObject(String date) {
        Date = date;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

}
